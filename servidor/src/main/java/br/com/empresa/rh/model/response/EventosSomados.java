package br.com.empresa.rh.model.response;

/**
 *
 * @author charles
 */
public class EventosSomados {
    private int id;
    private String nome;
    private double valor;

    public EventosSomados(int id, String nome, double valor) {
        this.id = id;
        this.nome = nome;
        this.valor = valor;
    }

    public int getId() {
        return id;
    }

    public String getNome() {
        return nome;
    }

    public double getValor() {
        return valor;
    }
    
    
}
